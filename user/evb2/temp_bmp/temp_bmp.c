void lcdDisplayInfo(float T, float p){
	char str[16];
	//LCDClear();
	sprintf(str, "Temp : %.1f C     ", T);;
	stringDisp(1,str, 16);
	sprintf(str, "P  :%.1f hPa     ", p);;
	stringDisp(2,str, 16);

}

